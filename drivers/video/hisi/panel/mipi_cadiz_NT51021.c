static int sony_candiz_blocks_select(unsigned long value)
{
    int ret  = -1;
    int length,i;
    int offset = 0;
    u8 module_clock_enable=0x40;
    u8 module_bypass=0;
    u8 p_vin_hdelay_ipc_low8;
    u8 p_vin_hdelay_ipc_u8;
    u8 data=0;
    struct module_enable_define *module_enable_value = NULL;

    struct cadiz_register_data *effect_area = NULL;

    if (lcd_ipc_bypass){
        ret = 0;
        return ret;
    }

    if (value >= 6){
        HISI_FB_ERR("%s: input value is error.\n",__func__);
        return -EINVAL;
    }

    if (lcd_board_type == 1)     //A04L for docomo
    {
        module_enable_value = &module_enable_define_table_dcm[value];
    }
    else   //A01L and else
    {
        module_enable_value = &module_enable_define_table[value];
    }

    HISI_FB_INFO("input value  =%d.\n",value);
    HISI_FB_INFO("module_enable_define= %d,%d,%d,%d,%d,%d,%d,%d,%d,%d,%d,%d,%d,0x%x,%d,%d,%d\n",
                 module_enable_value->p_crg_clk_pix_grc_gate_en,
                 module_enable_value->p_crg_clk_pix_spc_gate_en,
                 module_enable_value->p_crg_clk_pix_com_gate_en,
                 module_enable_value->p_crg_clk_pix_ice_gate_en,
                 module_enable_value->p_crg_clk_pix_ace_gate_en,
                 module_enable_value->p_crg_clk_pix_gty_gate_en,
                 module_enable_value->P_GRC_BYPASS,
                 module_enable_value->P_SPC_BYPASS,
                 module_enable_value->P_COM_BYPASS,
                 module_enable_value->P_ICE_BYPASS,
                 module_enable_value->P_ACE_BYPASS,
                 module_enable_value->P_GTY_BYPASS,
                 module_enable_value->P_IPC_BYPASS,
                 module_enable_value->P_SPCON,
                 module_enable_value->p_bdc_ice_ctrl_en,
                 module_enable_value->p_vin_share_en,
                 module_enable_value->p_vin_hdelay_ipc
    );

    module_clock_enable=module_clock_enable
                        |(module_enable_value->p_crg_clk_pix_gty_gate_en<<5)
                        |(module_enable_value->p_crg_clk_pix_grc_gate_en<<4)
                        |(module_enable_value->p_crg_clk_pix_spc_gate_en<<3)
                        |(module_enable_value->p_crg_clk_pix_com_gate_en<<2)
                        |(module_enable_value->p_crg_clk_pix_ice_gate_en<<1)
                        |(module_enable_value->p_crg_clk_pix_ace_gate_en);

    HISI_FB_INFO("[OK] :register module_clock_enable,value = 0x%01x.\n",module_clock_enable);

    module_bypass=(module_enable_value->P_GTY_BYPASS)
                 |(module_enable_value->P_GRC_BYPASS<<1)
                 |(module_enable_value->P_SPC_BYPASS<<2)
                 |(module_enable_value->P_COM_BYPASS<<3)
                 |(module_enable_value->P_ICE_BYPASS<<4)
                 |(module_enable_value->P_ACE_BYPASS<<5)
                 |(module_enable_value->P_IPC_BYPASS<<7);

    HISI_FB_INFO("[OK] :register module_bypass,value = 0x%01x.\n",module_bypass);

    p_vin_hdelay_ipc_low8 =(module_enable_value->p_vin_hdelay_ipc)&0xff;
    p_vin_hdelay_ipc_u8   =(module_enable_value->p_vin_hdelay_ipc>>8)&0xff;
    HISI_FB_INFO("[OK] :register p_vin_hdelay_ipc_u8,value = 0x%01x.\n",p_vin_hdelay_ipc_u8);
    HISI_FB_INFO("[OK] :register p_vin_hdelay_ipc_low8,value = 0x%01x.\n",p_vin_hdelay_ipc_low8);
    //Step1:enable modules clock
    cadiz_i2c_write_u16(0x0841,0x7f);

    cadiz_i2c_write_u16(0x0901,0x00);

    //Step2:set module register
    if (lcd_board_type == 1)     //A04L for docomo
    {
        cadiz_i2c_write_u16(0x090e,0xcc);
        cadiz_i2c_write_u16(0x090f,0x0a);//p_vin_bypass_change_tim=2764(0x0acc)
    }
    else
    {       //A01L and else
        cadiz_i2c_write_u16(0x090e,0xec);
        cadiz_i2c_write_u16(0x090f,0x0a); //p_vin_bypass_change_tim=2796(0x0aec)
    }

    cadiz_i2c_write_u16(0x0908,p_vin_hdelay_ipc_low8);
    cadiz_i2c_write_u16(0x0909,p_vin_hdelay_ipc_u8);


    cadiz_i2c_write_u16(0x0aff,module_bypass);
    if(module_enable_value->P_SPCON!=0xff){
        cadiz_i2c_read_u16(0x0ABE,&data);
        data=(data&0xf8)|module_enable_value->P_SPCON;
        cadiz_i2c_write_u16(0x0ABE,data);
    }


    cadiz_i2c_read_u16(0x0901,&data);
    data=(data&0xfd)|(module_enable_value->p_vin_share_en<<1);
    cadiz_i2c_write_u16(0x0901,data);

    cadiz_i2c_write_u16(0x0901,data|0x01);
    //Step3: delay
    //udelay(16600);
    mdelay(17);
    //Step4:
    cadiz_i2c_write_u16(0x0841,module_clock_enable);
    //checking for modules status
    ret = cadiz_i2c_read_u16(0xaff,&data);
    if(ret < 0){
        HISI_FB_ERR("[ERR] :register 0x0aff,value = 0x%x.\n",data);
    }else{
        HISI_FB_INFO("[OK] :register 0x0aff,value = 0x%x.\n",data);
    }
    ret = cadiz_i2c_read_u16(0x841,&data);
    if(ret < 0){
        HISI_FB_ERR("[ERR] :register 0x0841,value = 0x%x.\n",data);
    }else{
        HISI_FB_INFO("[OK] :register 0x0841,value = 0x%x.\n",data);
    }

}

