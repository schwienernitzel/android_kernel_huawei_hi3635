int  mipi_liszt_panel_enable_tp_sync(void)
{
    struct hisi_fb_data_type *hisifd = hisifd_test;
    int ret = 0;
    if(hisifd == NULL){
        HISI_FB_INFO("error, hisifd is NULL!\n");
        return ret;
    }
    /*id0: = 00(BOE);  id0: = 1(innolux);*/
    if (LCD_ID_TYPE_BOE == g_lcd_type){
        mipi_dsi_cmds_tx(tp_sync_enabel_cmds_boe, ARRAY_SIZE(tp_sync_enabel_cmds_boe), hisifd->mipi_dsi0_base);
        HISI_FB_INFO("enable BOE_NT51021 10.1' VIDEO TFT 1200 x 1920 TP_SYNC.\n");
    }else if (LCD_ID_TYPE_INX == g_lcd_type){
        mipi_dsi_cmds_tx(tp_sync_enabel_cmds_inx, ARRAY_SIZE(tp_sync_enabel_cmds_inx), hisifd->mipi_dsi0_base);
        HISI_FB_INFO("enable innolux_NT51021 10.1' VIDEO TFT 1200 x 1920 TP_SYNC.\n");
    }else{
        HISI_FB_INFO("error, no this panel, check the panel !\n");
    }
    return ret;
}

