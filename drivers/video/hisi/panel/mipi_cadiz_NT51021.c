{
    int ret = 0;
    u8 read_buf[2];
    struct i2c_client *client = g_sony_client;
    struct i2c_msg  msgs[] = {
        {
            .addr = client->addr,
            .flags = client->flags & I2C_M_TEN,
            .len = sizeof(read_buf),
            .buf = read_buf,
        },
        {
            .addr = client->addr,
            .flags = (client->flags & I2C_M_TEN) | I2C_M_RD,
            .len = 1,
            .buf = val,
        },
    };

    read_buf[0] = addr >> 8;
    read_buf[1] = (u8)(addr & 0xFF);

    ret = i2c_transfer(client->adapter, msgs, 2);
    if (ret != 2) {
        HISI_FB_ERR("read transfer error\n");
        ret = -EIO;
    } else {
        ret = 0;
    }
    return ret;
}


